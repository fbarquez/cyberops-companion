# Available languages
LANGUAGES = {
    "de": DE,
    "en": EN,
}

DEFAULT_LANGUAGE = "de"


def get_text(key: str, lang: str = DEFAULT_LANGUAGE) -> Any:
    """
    Get translated text by dot-notation key.

    Example: get_text("home.welcome", "de") -> "Willkommen bei ISORA"
    """
    translations = LANGUAGES.get(lang, LANGUAGES[DEFAULT_LANGUAGE])

    keys = key.split(".")
    value = translations

    for k in keys:
        if isinstance(value, dict) and k in value:
            value = value[k]
        else:
            # Fallback to English if key not found
            value = LANGUAGES["en"]
            for k2 in keys:
                if isinstance(value, dict) and k2 in value:
                    value = value[k2]
                else:
                    return key  # Return key if not found
            return value

    return value


def t(key: str, lang: str = DEFAULT_LANGUAGE) -> Any:
    """Shorthand for get_text."""
    return get_text(key, lang)
